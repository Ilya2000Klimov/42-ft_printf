#include "libft.h"

size_t		ft_uintlen(uintmax_t n)
{
	size_t	c;

	c = 0;
	if (n == 18446744073709551615U)
		return (20);
	while (n > 0)
	{
		n /= 10;
		c++;
	}
	return (c);
}